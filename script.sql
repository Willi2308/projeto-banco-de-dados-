CREATE OR REPLACE PACKAGE BODY pkg_biblioteca AS


    FUNCTION calcular_multa (
        p_id_emprestimo NUMBER
    )

    RETURN NUMBER

    IS

        CURSOR c_emprestimo IS
            SELECT data_prevista
            FROM Emprestimos
            WHERE id_emprestimo = p_id_emprestimo;

        v_data_prevista DATE;

        v_dias_atraso NUMBER;

        v_multa NUMBER(10,2);

    BEGIN

        OPEN c_emprestimo;

        FETCH c_emprestimo
        INTO v_data_prevista;

        CLOSE c_emprestimo;


        -- VALIDAR EMPRESTIMO

        IF v_data_prevista IS NULL THEN

            RAISE_APPLICATION_ERROR(
                -20005,
                'Emprestimo nao encontrado.'
            );

        END IF;


        v_dias_atraso :=
            TRUNC(SYSDATE - v_data_prevista);


        IF v_dias_atraso > 0 THEN

            v_multa := v_dias_atraso * 2;

        ELSE

            v_multa := 0;

        END IF;


        RETURN v_multa;

    END calcular_multa;




    PROCEDURE cadastrar_usuario (
        p_nome VARCHAR2,
        p_cpf VARCHAR2,
        p_telefone VARCHAR2,
        p_email VARCHAR2
    )

    IS

    BEGIN

        -- VALIDAR CAMPOS OBRIGATORIOS

        IF p_nome IS NULL
        OR p_cpf IS NULL THEN

            RAISE_APPLICATION_ERROR(
                -20007,
                'Dados obrigatorios nao informados.'
            );

        END IF;


        INSERT INTO Usuarios (
            nome,
            cpf,
            telefone,
            email
        )

        VALUES (
            p_nome,
            p_cpf,
            p_telefone,
            p_email
        );

        DBMS_OUTPUT.PUT_LINE(
            'Usuario cadastrado.'
        );

    EXCEPTION

        -- CPF DUPLICADO

        WHEN DUP_VAL_ON_INDEX THEN

            RAISE_APPLICATION_ERROR(
                -20006,
                'CPF ja cadastrado.'
            );

    END cadastrar_usuario;



    PROCEDURE consultar_usuarios

    IS

        CURSOR c_usuarios IS
            SELECT *
            FROM Usuarios;

    BEGIN

        FOR usuario IN c_usuarios LOOP

            DBMS_OUTPUT.PUT_LINE(
                'ID: ' || usuario.id_usuario ||
                ' | Nome: ' || usuario.nome ||
                ' | CPF: ' || usuario.cpf ||
                ' | Telefone: ' || usuario.telefone ||
                ' | Email: ' || usuario.email
            );

        END LOOP;

    END consultar_usuarios;




    PROCEDURE cadastrar_livro (
        p_titulo VARCHAR2,
        p_autor VARCHAR2,
        p_categoria VARCHAR2,
        p_quantidade NUMBER
    )

    IS

    BEGIN

        -- Validar título obrigatório
        IF p_titulo IS NULL THEN

            RAISE_APPLICATION_ERROR(
                -20009,
                'Titulo obrigatorio.'
            );

        END IF;


        -- Validar autor obrigatório
        IF p_autor IS NULL THEN

            RAISE_APPLICATION_ERROR(
                -20010,
                'Autor obrigatorio.'
            );

        END IF;


        -- Validar quantidade maior que zero
        IF p_quantidade <= 0 THEN

            RAISE_APPLICATION_ERROR(
                -20008,
                'Quantidade deve ser maior que zero.'
            );

        END IF;


        INSERT INTO Livros (
            titulo,
            autor,
            categoria,
            quantidade_total,
            quantidade_disponivel
        )

        VALUES (
            p_titulo,
            p_autor,
            p_categoria,
            p_quantidade,
            p_quantidade
        );

        DBMS_OUTPUT.PUT_LINE(
            'Livro cadastrado.'
        );

    END cadastrar_livro;



    PROCEDURE consultar_livros

    IS

        CURSOR c_livros IS
            SELECT *
            FROM Livros;

    BEGIN

        FOR livro IN c_livros LOOP

            DBMS_OUTPUT.PUT_LINE(
                'ID: ' || livro.id_livro ||
                ' | Titulo: ' || livro.titulo ||
                ' | Autor: ' || livro.autor ||
                ' | Categoria: ' || livro.categoria ||
                ' | Quantidade: ' || livro.quantidade_total ||
                ' | Disponiveis: ' || livro.quantidade_disponivel
            );

        END LOOP;

    END consultar_livros;




    PROCEDURE atualizar_livro (
        p_id_livro NUMBER,
        p_titulo VARCHAR2,
        p_autor VARCHAR2,
        p_categoria VARCHAR2,
        p_quantidade NUMBER
    )

    IS

    BEGIN

        UPDATE Livros

        SET titulo = p_titulo,
            autor = p_autor,
            categoria = p_categoria,
            quantidade_total = p_quantidade

        WHERE id_livro = p_id_livro;


        -- Validar se o livro existe
        IF SQL%ROWCOUNT = 0 THEN

            RAISE_APPLICATION_ERROR(
                -20004,
                'Livro nao encontrado.'
            );

        END IF;


        DBMS_OUTPUT.PUT_LINE(
            'Livro atualizado.'
        );

    END atualizar_livro;



    PROCEDURE excluir_livro (
        p_id_livro NUMBER
    )

    IS

        v_titulo Livros.titulo%TYPE;

    BEGIN

        SELECT titulo
        INTO v_titulo
        FROM Livros
        WHERE id_livro = p_id_livro;

        DELETE FROM Devolucoes
        WHERE id_emprestimo IN (
            SELECT id_emprestimo
            FROM Emprestimos
            WHERE id_livro = p_id_livro
        );

        DELETE FROM Emprestimos
        WHERE id_livro = p_id_livro;

        DELETE FROM Livros
        WHERE id_livro = p_id_livro;

        DBMS_OUTPUT.PUT_LINE(
            'Livro removido: ' || v_titulo
        );

    EXCEPTION

        -- Livro não encontrado
        WHEN NO_DATA_FOUND THEN

            RAISE_APPLICATION_ERROR(
                -20004,
                'Livro nao encontrado.'
            );

    END excluir_livro;


    PROCEDURE registrar_emprestimo (
        p_cpf VARCHAR2,
        p_id_livro NUMBER,
        p_prazo_dias NUMBER
    )

    IS

        v_id_usuario NUMBER;

        v_data_prevista DATE;

        v_quantidade NUMBER;

    BEGIN

        -- VALIDAR CPF

        BEGIN

            SELECT id_usuario
            INTO v_id_usuario
            FROM Usuarios
            WHERE cpf = p_cpf;

        EXCEPTION

            WHEN NO_DATA_FOUND THEN

                RAISE_APPLICATION_ERROR(
                    -20003,
                    'CPF nao cadastrado.'
                );

        END;


        -- VALIDAR LIVRO

        BEGIN

            SELECT quantidade_disponivel
            INTO v_quantidade
            FROM Livros
            WHERE id_livro = p_id_livro;

        EXCEPTION

            WHEN NO_DATA_FOUND THEN

                RAISE_APPLICATION_ERROR(
                    -20004,
                    'Livro nao encontrado.'
                );

        END;


        -- VALIDAR ESTOQUE

        IF v_quantidade <= 0 THEN

            RAISE_APPLICATION_ERROR(
                -20002,
                'Livro indisponivel.'
            );

        END IF;


        -- VALIDAR PRAZO

        IF p_prazo_dias < 7
        OR p_prazo_dias > 30 THEN

            RAISE_APPLICATION_ERROR(
                -20001,
                'Prazo permitido e de 7 a 30 dias.'
            );

        END IF;


        -- CALCULAR DATA PREVISTA

        v_data_prevista :=
            SYSDATE + p_prazo_dias;


        -- REGISTRAR EMPRESTIMO

        INSERT INTO Emprestimos (
            id_usuario,
            id_livro,
            data_emprestimo,
            prazo_dias,
            data_prevista,
            status_emprestimo
        )

        VALUES (
            v_id_usuario,
            p_id_livro,
            SYSDATE,
            p_prazo_dias,
            v_data_prevista,
            'EMPRESTADO'
        );


        DBMS_OUTPUT.PUT_LINE(
            'Emprestimo realizado.'
        );


        DBMS_OUTPUT.PUT_LINE(
            'Data prevista: ' ||
            TO_CHAR(v_data_prevista, 'DD/MM/YYYY')
        );

    END registrar_emprestimo;




    PROCEDURE consultar_emprestimos

    IS

        CURSOR c_emprestimos IS
            SELECT *
            FROM Emprestimos;

    BEGIN

        FOR emprestimo IN c_emprestimos LOOP

            DBMS_OUTPUT.PUT_LINE(
                'ID Emprestimo: ' || emprestimo.id_emprestimo ||
                ' | Usuario: ' || emprestimo.id_usuario ||
                ' | Livro: ' || emprestimo.id_livro ||
                ' | Data Empréstimo: ' || emprestimo.data_emprestimo ||
                ' | Prazo Dias: ' || emprestimo.prazo_dias ||
                ' | Data Prevista: ' || emprestimo.data_prevista ||
                ' | Status: ' || emprestimo.status_emprestimo
            );

        END LOOP;

    END consultar_emprestimos;




    PROCEDURE registrar_devolucao (
        p_cpf VARCHAR2,
        p_id_emprestimo NUMBER
    )

    IS

        v_id_usuario NUMBER;

        v_multa NUMBER(10,2);

        v_status Emprestimos.status_emprestimo%TYPE;

    BEGIN

        -- VALIDAR CPF

        BEGIN

            SELECT id_usuario
            INTO v_id_usuario
            FROM Usuarios
            WHERE cpf = p_cpf;

        EXCEPTION

            WHEN NO_DATA_FOUND THEN

                RAISE_APPLICATION_ERROR(
                    -20003,
                    'CPF nao cadastrado.'
                );

        END;


        -- VALIDAR EMPRESTIMO

        BEGIN

            SELECT status_emprestimo
            INTO v_status
            FROM Emprestimos
            WHERE id_emprestimo = p_id_emprestimo;

        EXCEPTION

            WHEN NO_DATA_FOUND THEN

                RAISE_APPLICATION_ERROR(
                    -20005,
                    'Emprestimo nao encontrado.'
                );

        END;


        -- VALIDAR SE JA FOI DEVOLVIDO

        IF v_status = 'DEVOLVIDO' THEN

            RAISE_APPLICATION_ERROR(
                -20009,
                'Emprestimo ja devolvido.'
            );

        END IF;


        -- CALCULAR MULTA

        v_multa :=
            calcular_multa(
                p_id_emprestimo
            );


        -- REGISTRAR DEVOLUCAO

        INSERT INTO Devolucoes (
            id_emprestimo,
            data_devolucao,
            multa
        )

        VALUES (
            p_id_emprestimo,
            SYSDATE,
            v_multa
        );


        -- ATUALIZAR STATUS

        UPDATE Emprestimos

        SET status_emprestimo = 'DEVOLVIDO'

        WHERE id_emprestimo = p_id_emprestimo;


        DBMS_OUTPUT.PUT_LINE(
            'Devolucao realizada.'
        );


        DBMS_OUTPUT.PUT_LINE(
            'Multa: R$ ' || v_multa
        );

    END registrar_devolucao;



    PROCEDURE consultar_devolucoes

    IS

        CURSOR c_devolucoes IS
            SELECT *
            FROM Devolucoes;

    BEGIN

        FOR devolucao IN c_devolucoes LOOP

            DBMS_OUTPUT.PUT_LINE(
                'ID Devolucao: ' || devolucao.id_devolucao ||
                ' | Emprestimo: ' || devolucao.id_emprestimo ||
                ' | Multa: R$ ' || devolucao.multa
            );

        END LOOP;

    END consultar_devolucoes;


END pkg_biblioteca;
