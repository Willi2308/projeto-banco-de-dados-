CREATE OR REPLACE TRIGGER trg_diminuir_livro

AFTER INSERT ON Emprestimos

FOR EACH ROW

BEGIN

    UPDATE Livros

    SET quantidade_disponivel =
        quantidade_disponivel - 1

    WHERE id_livro = :NEW.id_livro;

END;
/

CREATE OR REPLACE TRIGGER trg_aumentar_livro

AFTER INSERT ON Devolucoes

FOR EACH ROW

BEGIN

    UPDATE Livros

    SET quantidade_disponivel =
        quantidade_disponivel + 1

    WHERE id_livro = (

        SELECT id_livro

        FROM Emprestimos

        WHERE id_emprestimo =
            :NEW.id_emprestimo

    );

END;
