CREATE TABLE Usuarios (
    id_usuario NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR2(100) NOT NULL,
    cpf VARCHAR2(14) UNIQUE NOT NULL,
    telefone VARCHAR2(20),
    email VARCHAR2(100)
);


CREATE TABLE Livros (
    id_livro NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titulo VARCHAR2(150) NOT NULL,
    autor VARCHAR2(100) NOT NULL,
    categoria VARCHAR2(50),
    quantidade_total NUMBER NOT NULL,
    quantidade_disponivel NUMBER NOT NULL
);


CREATE TABLE Emprestimos (
    id_emprestimo NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_usuario NUMBER NOT NULL,
    id_livro NUMBER NOT NULL,
    data_emprestimo DATE DEFAULT SYSDATE,
    prazo_dias NUMBER NOT NULL,
    data_prevista DATE NOT NULL,
    status_emprestimo VARCHAR2(20) DEFAULT 'EMPRESTADO',

    CONSTRAINT fk_usuario
        FOREIGN KEY (id_usuario)
        REFERENCES Usuarios(id_usuario),

    CONSTRAINT fk_livro
        FOREIGN KEY (id_livro)
        REFERENCES Livros(id_livro)
);


CREATE TABLE Devolucoes (
    id_devolucao NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_emprestimo NUMBER NOT NULL,
    data_devolucao DATE DEFAULT SYSDATE,
    multa NUMBER(10,2),

    CONSTRAINT fk_emprestimo
        FOREIGN KEY (id_emprestimo)
        REFERENCES Emprestimos(id_emprestimo)
);




